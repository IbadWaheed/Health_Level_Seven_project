package starhealthcaretech.DataTypes;

public class OSP {
    private String occurrenceSpanCode;
    private String occurrenceSpanStartDate;
    private String occurrenceSpanStopDate;
    public String getOccurrenceSpanCode() {
        return occurrenceSpanCode;
    }
    public void setOccurrenceSpanCode(String occurrenceSpanCode) {
        this.occurrenceSpanCode = occurrenceSpanCode;
    }
    public String getOccurrenceSpanStartDate() {
        return occurrenceSpanStartDate;
    }
    public void setOccurrenceSpanStartDate(String occurrenceSpanStartDate) {
        this.occurrenceSpanStartDate = occurrenceSpanStartDate;
    }
    public String getOccurrenceSpanStopDate() {
        return occurrenceSpanStopDate;
    }
    public void setOccurrenceSpanStopDate(String occurrenceSpanStopDate) {
        this.occurrenceSpanStopDate = occurrenceSpanStopDate;
    }

    

}
